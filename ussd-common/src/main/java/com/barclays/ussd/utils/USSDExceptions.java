private String ussdErrorCode;

/**
 * @param bmgCode
 * @param ussdErrorCode
 */
private USSDExceptions(final String bmgCode, final String ussdErrorCode) {
    this.bmgCode = bmgCode;
    this.ussdErrorCode = ussdErrorCode;
}

/**
 * @return String
 */
public String getBmgCode() {
    return bmgCode;
}

/**
 * @return String
 */
public String getUssdErrorCode() {
    return ussdErrorCode;
}

/**
 * @param bmgCode
 * @return String
 */
public static String getUssdErrorCodeforBMG(final String bmgCode) {
    for (final USSDExceptions exception : USSDExceptions.values()) {
	if (exception.getBmgCode().equalsIgnoreCase(bmgCode)) {
	    return exception.getUssdErrorCode();
	}
    }
    return bmgCode;
}

